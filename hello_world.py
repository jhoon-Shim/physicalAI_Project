import time, sys, os

def clear():
    os.system('cls' if os.name == 'nt' else 'clear')

def slow_print(text, delay=0.03):
    for ch in text:
        sys.stdout.write(ch)
        sys.stdout.flush()
        time.sleep(delay)
    print()

def reveal(lines, delay=0.05):
    for line in lines:
        print(line)
        time.sleep(delay)

def pause(s=1.0):
    time.sleep(s)


